# Python has a built-in list type named "list".
# List literals are written within square brackets[].
# List work similar to strings use the len() & square brackets[] to access data
# First element starts at index 0

colors = ['red', 'blue', 'green'];
print(colors[0]); # red
print(colors[2]);  # green
print(len(colors));  # 3

print()

for color in colors:
  print(color);
